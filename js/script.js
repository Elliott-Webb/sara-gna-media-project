function renderMostRead(listElement, articles) {
  listElement.innerHTML = '';
  articles.forEach(article => {
    const li = document.createElement('li');
    const button = document.createElement('button');
    button.className = 'link-button';
    button.type = 'button';
    button.dataset.articleId = article.id;
    button.textContent = article.title;
    li.appendChild(button);
    listElement.appendChild(li);
  });
}

function attachArticleListeners() {
  document.querySelectorAll('[data-article-id]').forEach(trigger => {
    trigger.addEventListener('click', openModalArticle);
  });
}

function openModalArticle(event) {
  const trigger = event.currentTarget;
  const articleId = trigger?.dataset?.articleId;
  if (!articleId) return;
  const article = ARTICLE_INDEX.get(articleId);
  if (!article) return;

  const media = article.heroImage || article.cardImage;
  if (modalRefs.image) {
    if (media && media.src) {
      modalRefs.image.src = media.src;
      modalRefs.image.alt = media.alt || '';
      modalRefs.image.parentElement?.classList.remove('is-hidden');
    } else {
      modalRefs.image.src = '';
      modalRefs.image.alt = '';
      modalRefs.image.parentElement?.classList.add('is-hidden');
    }
  }

  if (modalRefs.header) {
    modalRefs.header.textContent = article.title;
  }

  if (modalRefs.content) {
    modalRefs.content.scrollTop = 0;
    renderBlocks(article.blocks, modalRefs.content);
  }

  if (modalRefs.surface) {
    modalRefs.surface.scrollTop = 0;
  }
  if (modalRefs.modal) {
    modalRefs.modal.scrollTop = 0;
    modalRefs.modal.style.display = 'flex';
  }

  resetModalScrollPosition();
}

function closeModal() {
  if (modalRefs.modal) {
    modalRefs.modal.style.display = 'none';
  }
}

function renderBlocks(blocks, container) {
  removeAllChildNodes(container);
  blocks.forEach(block => {
    if (typeof block === 'string') {
      const paragraph = document.createElement('p');
      paragraph.className = 'article-block article-block--paragraph';
      paragraph.textContent = block;
      container.appendChild(paragraph);
      return;
    }
    if (!block || typeof block !== 'object') {
      return;
    }

    switch (block.type) {
      case 'image': {
        const figure = document.createElement('figure');
        figure.className = 'article-block article-block--image';
        const img = document.createElement('img');
        img.src = block.src;
        img.alt = block.alt || '';
        figure.appendChild(img);
        if (block.caption) {
          const caption = document.createElement('figcaption');
          caption.textContent = block.caption;
          figure.appendChild(caption);
        }
        container.appendChild(figure);
        break;
      }
      case 'video': {
        const wrapper = document.createElement('div');
        wrapper.className = 'article-block article-block--video';
        const iframe = document.createElement('iframe');
        iframe.src = buildVideoSrc(block.src);
        iframe.width = block.width || '560';
        iframe.height = block.height || '315';
        iframe.setAttribute('frameborder', '0');
        iframe.setAttribute('allowfullscreen', '');
        iframe.allowFullscreen = true;
        if ('loading' in iframe) {
          iframe.loading = block.loading || 'lazy';
        }
        iframe.referrerPolicy = block.referrerPolicy || 'strict-origin-when-cross-origin';
        iframe.title = block.title || 'Embedded video';
        iframe.setAttribute('allow', block.allow || 'accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share');
        wrapper.appendChild(iframe);
        if (block.caption) {
          const caption = document.createElement('p');
          caption.className = 'media-caption';
          caption.textContent = block.caption;
          wrapper.appendChild(caption);
        }
        container.appendChild(wrapper);
        break;
      }
      case 'embed': {
        const wrapper = document.createElement('div');
        wrapper.className = 'article-block article-block--embed';
        wrapper.innerHTML = block.html;
        if (block.caption) {
          const caption = document.createElement('p');
          caption.className = 'media-caption';
          caption.textContent = block.caption;
          wrapper.appendChild(caption);
        }
        container.appendChild(wrapper);
        break;
      }
      case 'quote': {
        const quote = document.createElement('blockquote');
        quote.className = 'article-block article-block--quote';
        quote.textContent = block.text || '';
        if (block.attribution) {
          const cite = document.createElement('cite');
          cite.textContent = block.attribution;
          quote.appendChild(document.createElement('br'));
          quote.appendChild(cite);
        }
        container.appendChild(quote);
        break;
      }
      default: {
        const fallback = document.createElement('p');
        fallback.className = 'article-block article-block--paragraph';
        fallback.textContent = block.text || '';
        container.appendChild(fallback);
      }
    }
  });
}

function articleBelongsToPage(article, page) {
  const collections = getArticleCollections(article);
  if (page === 'extras') {
    return collections.includes('extras');
  }
  if (page === 'home') {
    return collections.includes('home');
  }
  return true;
}

function getArticleCollections(article) {
  if (!article) {
    return ['home'];
  }
  const collections = Array.isArray(article.collections) ? article.collections.filter(Boolean) : [];
  return collections.length ? collections : ['home'];
}

function formatDate(isoDate) {
  if (!isoDate) {
    return '';
  }
  try {
    const formatter = new Intl.DateTimeFormat(undefined, {
      year: 'numeric',
      month: 'short',
      day: 'numeric',
    });
    return formatter.format(new Date(isoDate));
  } catch (error) {
    return isoDate;
  }
}

function removeAllChildNodes(parent) {
  if (!parent) return;
  while (parent.firstChild) {
    parent.removeChild(parent.firstChild);
  }
}

function resetModalScrollPosition() {
  const targets = [modalRefs.content, modalRefs.surface, modalRefs.modal].filter(Boolean);
  if (!targets.length) {
    return;
  }

  const instantScroll = (el) => {
    if (!el) return;
    if (typeof el.scrollTo === 'function') {
      el.scrollTo({ top: 0, left: 0, behavior: 'auto' });
    } else {
      el.scrollTop = 0;
    }
    el.scrollTop = 0;
  };

  targets.forEach(instantScroll);

  const schedule = (typeof window !== 'undefined' && typeof window.requestAnimationFrame === 'function')
    ? window.requestAnimationFrame.bind(window)
    : function (cb) { return setTimeout(cb, 0); };

  schedule(() => targets.forEach(instantScroll));
  setTimeout(() => targets.forEach(instantScroll), 100);
}
function buildVideoSrc(src) {
  if (!src) return '';
  try {
    const url = new URL(src, window.location.href);
    const isYouTube = /youtube(?:-nocookie)?\.com|youtu\.be/.test(url.hostname);
    if (isYouTube) {
      if (!url.pathname.startsWith('/embed/')) {
        const videoId = url.searchParams.get('v') || url.pathname.replace('/', '');
        if (videoId) {
          url.hostname = url.hostname.includes('youtube-nocookie.com') ? 'www.youtube-nocookie.com' : 'www.youtube.com';
          url.pathname = `/embed/${videoId}`;
        }
      }
      if (!url.searchParams.has('rel')) {
        url.searchParams.set('rel', '0');
      }
      const origin = window.location.origin;
      if (origin && origin !== 'null' && !url.searchParams.has('origin')) {
        url.searchParams.set('origin', origin);
      }
    }
    return url.toString();
  } catch (error) {
    return src;
  }
}
